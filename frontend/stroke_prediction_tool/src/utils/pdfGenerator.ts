import { jsPDF } from 'jspdf'
import type { PredictionResponse } from '../api'
import type { DiseaseDefinition } from '../diseaseConfig'

const formatLabel = (value: string) =>
  value
    .replaceAll('_', ' ')
    .replaceAll('(', ' (')
    .trim()

export const generatePDF = (
  result: PredictionResponse,
  inputData: Record<string, string>,
  definition: DiseaseDefinition,
) => {
  const doc = new jsPDF()
  const pageWidth = doc.internal.pageSize.getWidth()
  const pageHeight = doc.internal.pageSize.getHeight()
  const riskColor =
    result.risk_level === 'High'
      ? [239, 68, 68]
      : result.risk_level === 'Moderate'
        ? [245, 158, 11]
        : [16, 185, 129]

  const ensurePageSpace = (currentY: number, requiredHeight: number) => {
    if (currentY + requiredHeight > pageHeight - 20) {
      doc.addPage()
      return 20
    }
    return currentY
  }

  doc.setFillColor(30, 41, 59)
  doc.rect(0, 0, pageWidth, 40, 'F')

  doc.setTextColor(255, 255, 255)
  doc.setFontSize(24)
  doc.setFont('helvetica', 'bold')
  doc.text(`NeuroGuard AI - ${definition.title}`, 20, 25)

  doc.setFontSize(10)
  doc.setFont('helvetica', 'normal')
  doc.text(`Generated on: ${new Date().toLocaleDateString()}`, pageWidth - 60, 25)

  doc.setTextColor(30, 41, 59)
  doc.setFontSize(18)
  doc.setFont('helvetica', 'bold')
  doc.text(`1. ${definition.title} Summary`, 20, 60)

  doc.setFontSize(14)
  doc.text(`Resulting Risk Score: ${result.risk_score}%`, 25, 75)

  doc.setDrawColor(riskColor[0], riskColor[1], riskColor[2])
  doc.setLineWidth(1)
  doc.setFillColor(riskColor[0], riskColor[1], riskColor[2], 0.1)
  doc.roundedRect(20, 82, pageWidth - 40, 20, 3, 3, 'FD')

  doc.setTextColor(riskColor[0], riskColor[1], riskColor[2])
  doc.setFont('helvetica', 'bold')
  doc.text(`LEVEL: ${result.risk_level.toUpperCase()}`, pageWidth / 2, 95, { align: 'center' })

  doc.setTextColor(30, 41, 59)
  doc.setFontSize(18)
  doc.text('2. Submitted Inputs', 20, 120)

  doc.setFontSize(11)
  doc.setFont('helvetica', 'normal')

  let y = 135
  Object.entries(inputData)
    .filter(([, value]) => value !== '')
    .forEach(([label, value]) => {
      y = ensurePageSpace(y, 12)
      doc.setFont('helvetica', 'bold')
      doc.text(`${formatLabel(label)}:`, 25, y)
      doc.setFont('helvetica', 'normal')
      const wrappedValue = doc.splitTextToSize(String(value), pageWidth - 115)
      doc.text(wrappedValue, 92, y)
      y += Math.max(10, wrappedValue.length * 6)
    })

  y += 8
  y = ensurePageSpace(y, 30)
  doc.setFontSize(18)
  doc.setFont('helvetica', 'bold')
  doc.text('3. Key Factors', 20, y)

  y += 15
  doc.setFontSize(11)
  doc.setFont('helvetica', 'normal')
  result.top_factors.forEach((factor) => {
    y = ensurePageSpace(y, 12)
    const wrappedFactor = doc.splitTextToSize(`• ${factor}`, pageWidth - 45)
    doc.text(wrappedFactor, 25, y)
    y += Math.max(10, wrappedFactor.length * 6)
  })

  y += 10
  y = ensurePageSpace(y, 30)
  doc.setFontSize(18)
  doc.setFont('helvetica', 'bold')
  doc.text('4. Recommendations', 20, y)

  y += 15
  doc.setFontSize(11)
  doc.setFont('helvetica', 'normal')
  definition.recommendations.forEach((item) => {
    y = ensurePageSpace(y, 14)
    const wrappedRecommendation = doc.splitTextToSize(`• ${item}`, pageWidth - 45)
    doc.text(wrappedRecommendation, 25, y)
    y += Math.max(12, wrappedRecommendation.length * 6)
  })

  y = ensurePageSpace(y + 8, 20)
  doc.setFontSize(8)
  doc.setTextColor(150, 150, 150)
  const disclaimer =
    'Disclaimer: This report is generated by an AI model for informational purposes only and does not constitute professional medical advice, diagnosis, or treatment. Always seek the advice of your physician or other qualified health provider with any questions you may have regarding a medical condition.'
  doc.text(doc.splitTextToSize(disclaimer, pageWidth - 40), 20, y + 8)

  doc.save(`NeuroGuard_${definition.slug}_risk_report.pdf`)
}
