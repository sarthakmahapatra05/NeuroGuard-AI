import { jsPDF } from 'jspdf';

export const generatePDF = (result: any, inputData: any) => {
  const doc = new jsPDF();
  const pageWidth = doc.internal.pageSize.getWidth();
  
  // Header
  doc.setFillColor(30, 41, 59);
  doc.rect(0, 0, pageWidth, 40, 'F');
  
  doc.setTextColor(255, 255, 255);
  doc.setFontSize(24);
  doc.setFont('helvetica', 'bold');
  doc.text('NeuroGuard AI - Risk Assessment', 20, 25);
  
  doc.setFontSize(10);
  doc.setFont('helvetica', 'normal');
  doc.text(`Generated on: ${new Date().toLocaleDateString()}`, pageWidth - 60, 25);
  
  // Risk Summary
  doc.setTextColor(30, 41, 59);
  doc.setFontSize(18);
  doc.setFont('helvetica', 'bold');
  doc.text('1. Stroke Risk Summary', 20, 60);
  
  const riskColor = result.risk_level === 'High' ? [239, 68, 68] : result.risk_level === 'Moderate' ? [245, 158, 11] : [16, 185, 129];
  
  doc.setFontSize(14);
  doc.text(`Resulting Risk Score: ${result.risk_score}%`, 25, 75);
  
  doc.setDrawColor(riskColor[0], riskColor[1], riskColor[2]);
  doc.setLineWidth(1);
  doc.setFillColor(riskColor[0], riskColor[1], riskColor[2], 0.1);
  doc.roundedRect(20, 82, pageWidth - 40, 20, 3, 3, 'FD');
  
  doc.setTextColor(riskColor[0], riskColor[1], riskColor[2]);
  doc.setFont('helvetica', 'bold');
  doc.text(`LEVEL: ${result.risk_level.toUpperCase()}`, pageWidth / 2, 95, { align: 'center' });
  
  // Patient Data
  doc.setTextColor(30, 41, 59);
  doc.setFontSize(18);
  doc.text('2. Patient Input Data', 20, 120);
  
  doc.setFontSize(11);
  doc.setFont('helvetica', 'normal');
  const patientData = [
    ['Age', inputData.age],
    ['Hypertension', inputData.hypertension === '1' ? 'Yes' : 'No'],
    ['Heart Disease', inputData.heart_disease === '1' ? 'Yes' : 'No'],
    ['Avg Glucose Level', `${inputData.avg_glucose_level} mg/dL`],
    ['BMI', inputData.bmi],
    ['Smoking Status', inputData.smoking_status]
  ];
  
  let y = 135;
  patientData.forEach(([label, value]) => {
    doc.setFont('helvetica', 'bold');
    doc.text(`${label}:`, 25, y);
    doc.setFont('helvetica', 'normal');
    doc.text(`${value}`, 80, y);
    y += 10;
  });
  
  // Key Factors & Insights
  doc.setFontSize(18);
  doc.setFont('helvetica', 'bold');
  doc.text('3. Key Factors & Insights', 20, 205);
  
  doc.setFontSize(11);
  doc.setFont('helvetica', 'normal');
  y = 220;
  result.top_factors.forEach((factor: string) => {
    doc.text(`• ${factor}`, 25, y);
    y += 10;
  });
  
  // Disclaimer
  doc.setFontSize(8);
  doc.setTextColor(150, 150, 150);
  const disclaimer = 'Disclaimer: This report is generated by an AI model for informational purposes only and does not constitute professional medical advice, diagnosis, or treatment. Always seek the advice of your physician or other qualified health provider with any questions you may have regarding a medical condition.';
  doc.text(doc.splitTextToSize(disclaimer, pageWidth - 40), 20, pageWidth > 280 ? 280 : 270);
  
  // Save the PDF
  doc.save('NeuroGuard_Stroke_Risk_Report.pdf');
};
